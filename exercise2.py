
def add(n1, n2):
    return n1 + n2
# تمرین دو عدد از ورودی دریافت نمائید و با کمک توابع زیر حاصل 
# حمع، تفریق، تقسیم و ضرب آنها را نمایش دهد
# sub  تفریق
# div  تقسیم
# mul ضرب
print(add(2,5))

# تمرین : تابعی بنویسید که یک عدد از ورودی دریافت نماید و در صورت زوج بودن
# True
# و در صورت فرد بودن 
# False
# را برگرداند

